#ifndef VARSTACKTEST_RNSTACK_H
#define VARSTACKTEST_RNSTACK_H

#include <vector>
#include <stack>

template<class T>
class RnStack
{
 public:
	explicit RnStack() = default;

	/*************************************************************************/
	~RnStack()
	{
		Clear();
	}

	/*************************************************************************/
	T Pop()
	{
		T item = Top();
		_data.pop_back();

		return item;
	}

	/*************************************************************************/
	void pop_back()
	{
		Pop();
	}

	/*************************************************************************/
	void Push(T value)
	{
		_data.push_back(value);
	}

	/*************************************************************************/
	void emplace_back(T value)
	{
		Push(value);
	}

	/*************************************************************************/
	void push_back(T value)
	{
		Push(value);
	}

	/*************************************************************************/
	long Size()
	{
		return _data.size();
	}

	/*************************************************************************/
	bool IsEmpty()
	{
		return _data.empty();
	}

	/*************************************************************************/
	T Top()
	{
		return _data.back();
	}

	/*************************************************************************/
	T back()
	{
		return Top();
	}

	/*************************************************************************/
	void Clear()
	{
		while (!_data.empty())
		{
			Pop();
		}
	}

	/*************************************************************************/
	std::vector<T> GetData()
	{
		return _data;
	}

	/*************************************************************************/
	void Reserve(size_t n)
	{
		_data.reserve(n);
	}

 private:
	std::vector<T> _data;

};

#endif //VARSTACKTEST_RNSTACK_H
